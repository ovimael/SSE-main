        <p>Pilares del Instituto Municipal de las Mujeres </p> <br>

        <p>ATENCIÓN, PREVENCIÓN, TRANSVERSALIZACIÓN
        <P>TRABAJO SOCIAL: Es el primer contacto con nuestras usuarias, por lo cual su intervención es fundamental para establecer e identificar las problemáticas y así brindar los servicios necesarios para mejorar las condiciones de vida de las mujeres que acuden al Instituto.

Gestión de recursos, canalización a distintas dependencias de los tres niveles de gobierno u organizaciones civiles, acompañamiento a mujeres víctimas de violencia en el proceso de denuncia e ingreso a refugios, así como apoyo en distintos trámites.
        <P>ATENCION PSICOLÓGICA: Nuestro equipo de terapeutas se enfoca en el manejo emocional de las circunstancias adversas para afrontar situaciones de violencia; con el objetivo de identificar e interrumpir los ciclos de violencia, buscamos fortalecer las redes de apoyo e impulsar el desarrollo de un mejor proyecto de vida de las mujeres que acuden el Instituto.
        <P>ASESORÍA JURÍDICA: El objetivo de esta área es dar acompañamiento, asesoría y representación legal a las mujeres en el ámbito legal familiar, desde una perspectiva de género, intercultural y apegada a los derechos humanos.

Algunos de los trámites donde podemos apoyar a nuestras usuarias son: divorcio voluntario, divorcio contencioso, guarda y custodia, pensión alimenticia, pérdida de patria potestad, adopción y jurisdicción voluntaria.

El acceso a protección legal y el acompañamiento en su proceso para el ejercicio de sus derechos, particularmente la protección a la integridad física, emocional y patrimonial, son parte integral de nuestro compromiso con las mujeres que acuden al Instituto.
Este es un espacio seguro y confidencial donde también niñas, niños y adolescentes puede recibir atención.
        <P>ENLACE INDÍGENA: Queremos atender y mejorar las condiciones de vida de las mujeres de comunidades indígenas y sus familias, propiciar su empoderamiento económico, así como apoyar su crecimiento académico, proporcionándoles herramientas formativas para impulsar su desarrollo personal y comunitario.

Otro objetivo fundamental es fomentar la visibilización y el reconocimiento de nuestros pueblos originarios, sus tradiciones, costumbres, creencias y ritos, una de nuestras estrategias para lograrlo es la organización del Festival Indígena Umukí.
